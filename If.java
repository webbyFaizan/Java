public class If {
    public static void main(String[] args) {
        System.out.println("------If Statement------");
        if (2 < 4){
            System.out.println("2 is smaller than 4");
        }

        System.out.println();

        System.out.println("------Nested if------");
        if (10 < 20){
            System.out.println("10 is smaller");
        }
    }
}
